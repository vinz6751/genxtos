    {
        *dst++ = *src++;
        *dst++ = *src++;
        *dst++ = *src++;
        *dst++ = *src++;
    }
    for (i=0; i < COL_COUNT/4; i++)
    {
        *dst++ = ' ';
        *dst++ = ' ';
        *dst++ = ' ';
        *dst++ = ' ';
    }
#else
    // This doesn't work because it uses 16/32 byte writes
